package br.com.sistemafaculdade.testes;
import br.com.sistemafaculdade.*;

public class Vinculos {
	private double salario;
	private double adc;
	

	public Vinculos(double salario, double adc) {
		super();
		this.salario = salario;
		this.adc = adc;
	}

	public double getAdc() {
		return salario + adc;
	}

	
	


}
